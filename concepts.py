# Lists - > Ordered Collection of items. It is mutable

list1 = [1,2,3,[4,5],["apple"],"banana",3.24]
print(list1)

# Tuple - > Ordered Collection of items. It is immutable.

tuple1 = (1,2,3,("Himanshu"))
print(tuple1)

tuple1 = (5,6,7)    #Will check this again in tuples
print(tuple1)


# Dictionary

dict1 = {"name":"Himanshu","age":23}
print(dict1)

name = "Himanshu"
print(len(name))

# Strings in python are immutable
print(name.upper())
print(name)

# split() method split the string at a given point that we decide, and returns a list of splitted parts.
print(name.split("a"))